
let fecha = new Date(); // Muestra la fecha y hora actual.

console.log(fecha);

console.log(fecha.getDate()); // Muestra el día del mes.

console.log(fecha.getDay()); // Muestra el día de la semana.

console.log(fecha.getMonth()); // Muestra el mes.

console.log(fecha.getFullYear()); // Muestra el año.

console.log(fecha.getHours()); // Muestra la hora.

console.log(fecha.getMinutes()); // Muestra los minutos.

console.log(fecha.getSeconds()); // Muestra los segundos.

console.log(fecha.getMilliseconds()); // Muestra los milisegundos.

console.log(fecha.toString()); // Muestra la fecha en formato string.

console.log(fecha.toDateString()); // Muestra la fecha en formato string sin la hora.

console.log(fecha.toLocaleString()); // Muestra la fecha en formato string, pero en el idioma del sistema.

console.log(fecha.toLocaleDateString()); // Muestra solo la fecha en formato string con '/'.

console.log(fecha.toLocaleTimeString()); // Muestra la hora en formato string.

console.log(fecha.getTime()); // Muestra la fecha en milisegundos desde el 1 de enero de 1970.

console.log(fecha.getTimezoneOffset()); // Muestra la diferencia horaria con respecto a la hora de Greenwich.



